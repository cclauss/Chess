"""A short & simple class for all Phantom objects to inherit from.  

This file is import clean."""

class PhantomObj (object):
    
    name = "object"
    
    def __repr__(self):
        return "<{}.{} object at {}>".format(__name__, self.__class__.__name__, hex(id(self)))

